    {
//       je renvoi true si l'element en question appartion a la meme unite que le chef
        // et si l'utilisateur connecter est le chef
        $personnel = $model->getPersonnel();
        $typePersonnel = Str::lower($user->getPersonnelName());
        if($typePersonnel == "chef"){
            //je verifie si l'element n'est pas null verifie si il appartiennet a la meme unite
            if($personnel != null){
                //je verifie si il est dans une unite
                if($personnel->unite_id != null){
                    //alors je verifie si il apartient dans la meme unite que le model
                    if($personnel->unite_id == $user->getPersonnel()->unite_id){
                        return  true;
                    }
                }
            }
        }else{
            return false;
        }
        return false;
    }

